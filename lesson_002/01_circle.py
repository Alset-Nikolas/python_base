import math

# NOTE –∏–º–ø–æ—Ä—Ç–∏—Ä–æ–≤–∞—Ç—å –º–æ–¥—É–ª–∏ –≤—Å–µ–≥–¥–∞ –Ω—É–∂–Ω–æ –≤—ã—à–µ –≤—Å–µ—Ö –æ—Å—Ç–∞–ª—å–Ω—ã—Ö —Å—Ç—Ä–æ–∫ –∫–æ–¥–∞, –ø—É—Å—Ç—ã—Ö —Å—Ç—Ä–æ–∫ –∏ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–≤ (–∫—Ä–æ–º–µ –¥–≤—É—Ö –ø–µ—Ä–≤—ã—Ö)

# –ï—Å—Ç—å –∑–Ω–∞—á–µ–Ω–∏–µ —Ä–∞–¥–∏—É—Å–∞ –∫—Ä—É–≥–∞
radius = 42

# –í—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ –∫–æ–Ω—Å–æ–ª—å –∑–Ω–∞—á–µ–Ω–∏–µ –ø—Ä–æ—â–∞–¥–∏ —ç—Ç–æ–≥–æ –∫—Ä—É–≥–∞ —Å —Ç–æ—á–Ω–æ—Å—Ç—é –¥–æ 4-—Ö –∑–Ω–∞–∫–æ–≤ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π
# –ø–æ–¥—Å–∫–∞–∑–∫–∏:

# —Ñ–æ—Ä–º—É–ª—É –º–æ–∂–Ω–æ –ø–æ–¥—Å–º–æ—Ç—Ä–µ—Ç—å –≤ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç–µ,
# –ø–∏ –≤–æ–∑—å–º–∏—Ç–µ —Ä–∞–≤–Ω—ã–º 3.1415926
# —Ç–æ—á–Ω–æ—Å—Ç—å —É–∫–∞–∑—ã–≤–∞–µ—Ç—Å—è –≤ —Ñ—É–Ω–∫—Ü–∏–∏ round()


S = math.pi * radius ** 2

print(round(S, 4))

# –î–∞–ª–µ–µ, –ø—É—Å—Ç—å –µ—Å—Ç—å –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã —Ç–æ—á–∫–∏
point_1 = (23, 34)
# –≥–¥–µ 23 - –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞ —Ö, 34 - –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç–∞ —É
x_1, y_1 = point_1[0], point_1[1]
# –ï—Å–ª–∏ —Ç–æ—á–∫–∞ point –ª–µ–∂–∏—Ç –≤–Ω—É—Ç—Ä–∏ —Ç–æ–≥–æ —Å–∞–º–æ–≥–æ –∫—Ä—É–≥–∞ [—Ü–µ–Ω—Ç—Ä –≤ –Ω–∞—á–∞–ª–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç (0, 0), radius = 42],
# —Ç–æ –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ –∫–æ–Ω—Å–æ–ª—å True, –ò–ª–∏ False, –µ—Å–ª–∏ —Ç–æ—á–∫–∞ –ª–µ–∂–∏—Ç –≤–æ–≤–Ω–µ –∫—Ä—É–≥–∞.
# –ø–æ–¥—Å–∫–∞–∑–∫–∏:
# –Ω—É–∂–Ω–æ –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å —Ä–∞—Å—Å—Ç–æ—è–Ω–∏–µ –æ—Ç —ç—Ç–æ–π —Ç–æ—á–∫–∏ –¥–æ –Ω–∞—á–∞–ª–∞ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç (0, 0)
# —Ñ–æ—Ä–º—É–ª–∞ —Ç–∞–∫ –∂–µ –µ—Å—Ç—å –≤ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç–µ
# –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–π –∫–æ—Ä–µ–Ω—å - —ç—Ç–æ –≤–æ–∑–≤–µ–¥–µ–Ω–∏–µ –≤ —Å—Ç–µ–ø–µ–Ω—å 0.5
# –æ–ø–µ—Ä–∞—Ü–∏–∏ —Å—Ä–∞–≤–Ω–µ–Ω–∏—è –¥–∞—é—Ç –±—É–ª–µ–≤—ã –∫–æ–Ω—Å—Ç–∞–Ω—Ç—ã True –∏ False
vector_point_1 = (x_1 ** 2 + y_1 ** 2) ** 0.5
print(vector_point_1 <= radius)
# –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ –¥–ª—è –¥—Ä—É–≥–æ–π —Ç–æ—á–∫–∏
point_2 = (30, 30)
x_2, y_2 = point_2[0], point_2[1]
