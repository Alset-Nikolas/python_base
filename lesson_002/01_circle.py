# –ï—Å–ª–∏ —Ç–æ—á–∫–∞ point_2 –ª–µ–∂–∏—Ç –≤–Ω—É—Ç—Ä–∏ –∫—Ä—É–≥–∞ (radius = 42), —Ç–æ –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞ –∫–æ–Ω—Å–æ–ª—å True,
# –ò–ª–∏ False, –µ—Å–ª–∏ —Ç–æ—á–∫–∞ –ª–µ–∂–∏—Ç –≤–æ–≤–Ω–µ –∫—Ä—É–≥–∞.
print(vector_point_2 <= radius)

