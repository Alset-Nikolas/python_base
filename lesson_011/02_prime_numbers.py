class PrimeNumbers:

    def __init__(self, n):
        self.n = n
        self.i = -1
        self.prime_numbers = []

    def __iter__(self):
        self.i = -1
        return self

    def __next__(self):
        self.i += 1
        if self.i < self.n:
            for number in range(2, self.n + 1):
                for prime in self.prime_numbers:
                    if number % prime == 0:
                        break
                else:
                    self.prime_numbers.append(number)
                    return number

        raise StopIteration


prime_number_iterator = PrimeNumbers(n=10000)
for number in prime_number_iterator:
    print(number)

