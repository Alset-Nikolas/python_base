def function_filter_happy(number):
    number_str = str(number)
    if len(number_str) % 2 == 0:
        left = number_str[:len(number_str) // 2]
        right = number_str[len(number_str) // 2:]
    else:
        left = number_str[:len(number_str) // 2]
        right = number_str[len(number_str) // 2 + 1:]
    a = 0
    b = 0
    for iter in left:
        a += int(iter)
    for iter in right:
        b += int(iter)
    if a == b:
        return True
    return False


