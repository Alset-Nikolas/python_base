def prime_numbers_generator(n):
    sito = [0] * n
    for i in range(n):
        if i == 0 or i == 1:
            sito[i] = -1
        elif sito[i] == 0:
            sito[i] = 1
            yield i
            k = 2
            while i * k < n:
                sito[i * k] = -1
                k += 1


prime_numbers_generator(n=100)

for number in prime_numbers_generator(n=10000):
    print(number)

